package org.hfut.model;

public class CloseProjectInfo {
    private double allget;
    private double allcost;
    private double allreceive;
    private double perget;
    private double percost;
    private double perreceive;
    private int month;

    public double getAllget() {
        return allget;
    }

    public void setAllget(double allget) {
        this.allget = allget;
    }

    public double getAllcost() {
        return allcost;
    }

    public void setAllcost(double allcost) {
        this.allcost = allcost;
    }

    public double getAllreceive() {
        return allreceive;
    }

    public void setAllreceive(double allreceive) {
        this.allreceive = allreceive;
    }

    public double getPerget() {
        return perget;
    }

    public void setPerget(double perget) {
        this.perget = perget;
    }

    public double getPercost() {
        return percost;
    }

    public void setPercost(double percost) {
        this.percost = percost;
    }

    public double getPerreceive() {
        return perreceive;
    }

    public void setPerreceive(double perreceive) {
        this.perreceive = perreceive;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }
}
